                                       if_else(x <= 0.3048, 0.26 + (x - 0.2438) / (0.3048 - 0.2438) * (0.37 - 0.26),
                                               if_else(x <= 0.3658, 0.37 + (x - 0.3048) / (0.3658 - 0.3048) * (0.48 - 0.37),
                                                       if_else(x <= 0.4267, 0.48 + (x - 0.3658) / (0.4267 - 0.3658) * (0.59 - 0.48),
                                                               if_else(x <= 0.4877, 0.59 + (x - 0.4267) / (0.4877 - 0.4267) * (0.68 - 0.59),
                                                                       if_else(x <= 0.5486, 0.68 + (x - 0.4877) / (0.5486 - 0.4877) * (0.76 - 0.68),
                                                                               if_else(x <= 0.6096, 0.76 + (x - 0.5486) / (0.6096 - 0.5486) * (1 - 0.76),
                                                                                       if_else(x <= 3.048, 1 + (x - 0.6096) / (3.048 - 0.6096) * (1 - 1),
                                                                                               NA
                                                                                       )
                                                                               )
                                                                       )
                                                               )
                                                       )
                                               )
                                       )
                               )
                       )
               ))
  return(y)
}

